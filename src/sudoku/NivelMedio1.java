    private void btnVerificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerificarActionPerformed
        boolean espacoEmBranco = isEspacoEmBranco();
        if (espacoEmBranco) {
            new EspacoEmBranco(this);
        } else {
            int countAcertos = 0;
            if (Integer.parseInt(tfBloco1.getText()) == 3) {
                countAcertos += 1;
            }
            if (Integer.parseInt(tfBloco2.getText()) == 2) {
                countAcertos += 1;
            }
            if (Integer.parseInt(tfBloco4.getText()) == 4) {
                countAcertos += 1;
            }
            if (Integer.parseInt(tfBloco6.getText()) == 1) {
                countAcertos += 1;
            }
            if (Integer.parseInt(tfBloco7.getText()) == 2) {
                countAcertos += 1;
            }
            if (Integer.parseInt(tfBloco8.getText()) == 3) {
                countAcertos += 1;
            }
            if (Integer.parseInt(tfBloco9.getText()) == 1) {
                countAcertos += 1;
            }
            if (Integer.parseInt(tfBloco10.getText()) == 4) {
                countAcertos += 1;
            }
            if (Integer.parseInt(tfBloco11.getText()) == 3) {
                countAcertos += 1;
            }
            if (Integer.parseInt(tfBloco13.getText()) == 2) {
                countAcertos += 1;
            }
            if (Integer.parseInt(tfBloco15.getText()) == 4) {
                countAcertos += 1;
            }
            if (Integer.parseInt(tfBloco16.getText()) == 1) {
                countAcertos += 1;
            }
            
            if (countAcertos == 12) {
                if (escolhaUsuario.isDesafio()) {
                    this.dispose();
                    new NivelDificil1("desafio").setVisible(true);
                } else if (escolhaUsuario.isIndividual()) {
                    pontuacao.vitoriaIndividual(this);
                }
            } else {
                JOptionPane.showMessageDialog(this, "QUE PENA, TENTE NOVAMENTE");
                tfBloco1.setText("");
                tfBloco2.setText("");
                tfBloco4.setText("");
                tfBloco6.setText("");
                tfBloco7.setText("");
                tfBloco8.setText("");
                tfBloco9.setText("");
                tfBloco10.setText("");
                tfBloco11.setText("");
                tfBloco13.setText("");
                tfBloco15.setText("");
                tfBloco16.setText("");
            }
        }
    }//GEN-LAST:event_btnVerificarActionPerformed

